-- Не даём клиенту менять баланс обычным update users.
create or replace function public.telechat_guard_moons()
returns trigger language plpgsql set search_path=public as $$
begin
  if new.moons is distinct from old.moons
     and coalesce(current_setting('telechat.moon_write',true),'') <> 'allowed' then
    raise exception 'Баланс Лун изменяется только через серверную функцию';
  end if;
  return new;
end;
$$;

drop trigger if exists telechat_guard_users_moons on public.users;
create trigger telechat_guard_users_moons
before update of moons on public.users
for each row execute function public.telechat_guard_moons();

create or replace function public.telechat_transfer_moons(
  p_actor_nick text, p_target_nick text, p_amount bigint, p_note text default ''
)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v_actor text:=lower(trim(coalesce(p_actor_nick,'')));
  v_target text:=lower(trim(coalesce(p_target_nick,'')));
  v_balance bigint; v_target_exists boolean; v_now bigint:=(extract(epoch from clock_timestamp())*1000)::bigint;
begin
  if p_amount is null or p_amount<1 or p_amount>1000000000 then raise exception 'Некорректная сумма'; end if;
  if v_actor=v_target then raise exception 'Нельзя перевести Луны самому себе'; end if;
  select moons into v_balance from public.users where lower(nick)=v_actor for update;
  select exists(select 1 from public.users where lower(nick)=v_target) into v_target_exists;
  if v_balance is null then raise exception 'Отправитель не найден'; end if;
  if not v_target_exists then raise exception 'Получатель не найден'; end if;
  perform set_config('telechat.moon_write','allowed',true);
  if v_actor<>'creator' then
    update public.users set moons=moons-p_amount where lower(nick)=v_actor and moons>=p_amount returning moons into v_balance;
    if not found then raise exception 'Недостаточно Лун'; end if;
  end if;
  update public.users set moons=moons+p_amount where lower(nick)=v_target;
  insert into public.moon_transactions(from_nick,to_nick,amount,kind,note,created_at)
  values(v_actor,v_target,p_amount,'transfer',left(coalesce(p_note,''),120),v_now);
  return jsonb_build_object('ok',true,'balance',case when v_actor='creator' then 1000000000 else v_balance end,'amount',p_amount,'target',v_target);
end;
$$;

create or replace function public.telechat_send_gift(
  p_actor_nick text, p_target_nick text, p_gift_id text, p_message text default ''
)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v_actor text:=lower(trim(coalesce(p_actor_nick,'')));
  v_target text:=lower(trim(coalesce(p_target_nick,'')));
  v_gift public.moon_gifts%rowtype; v_balance bigint; v_target_exists boolean;
  v_now bigint:=(extract(epoch from clock_timestamp())*1000)::bigint;
  v_last_limited bigint; v_record_id bigint;
begin
  select * into v_gift from public.moon_gifts where id=p_gift_id;
  if v_gift.id is null then raise exception 'Подарок не найден'; end if;
  if v_gift.limited and coalesce(v_gift.available_until,0)<=v_now then raise exception 'Лимитированная коллекция уже закрыта'; end if;
  select moons into v_balance from public.users where lower(nick)=v_actor for update;
  select exists(select 1 from public.users where lower(nick)=v_target) into v_target_exists;
  if v_balance is null then raise exception 'Отправитель не найден'; end if;
  if not v_target_exists then raise exception 'Получатель не найден'; end if;
  if v_gift.limited then
    select max(created_at) into v_last_limited from public.user_gifts where lower(sender_nick)=v_actor and limited=true;
    if coalesce(v_last_limited,0)>v_now-1814400000 then
      raise exception 'Лимитированный подарок доступен один раз в 21 день';
    end if;
  end if;
  perform set_config('telechat.moon_write','allowed',true);
  if v_actor<>'creator' then
    update public.users set moons=moons-v_gift.price where lower(nick)=v_actor and moons>=v_gift.price returning moons into v_balance;
    if not found then raise exception 'Недостаточно Лун'; end if;
  end if;
  insert into public.user_gifts(owner_nick,sender_nick,gift_id,gift_name,gift_icon,price,rarity,theme,limited,message,created_at)
  values(v_target,v_actor,v_gift.id,v_gift.name,v_gift.icon,v_gift.price,v_gift.rarity,v_gift.theme,v_gift.limited,left(coalesce(p_message,''),120),v_now)
  returning id into v_record_id;
  insert into public.moon_transactions(from_nick,to_nick,amount,kind,gift_id,note,created_at)
  values(v_actor,v_target,v_gift.price,'gift',v_gift.id,left(coalesce(p_message,''),120),v_now);
  return jsonb_build_object('ok',true,'balance',case when v_actor='creator' then 1000000000 else v_balance end,'gift_record_id',v_record_id,'gift_id',v_gift.id,'limited',v_gift.limited,'next_limited_at',case when v_gift.limited then v_now+1814400000 else null end);
end;
$$;

create or replace function public.telechat_creator_moons(
  p_actor_nick text, p_target_nick text, p_amount bigint, p_note text default ''
)
returns jsonb language plpgsql security definer set search_path=public as $$
declare
  v_actor text:=lower(trim(coalesce(p_actor_nick,'')));
  v_target text:=lower(trim(coalesce(p_target_nick,'')));
  v_balance bigint; v_now bigint:=(extract(epoch from clock_timestamp())*1000)::bigint;
begin
  if v_actor<>'creator' then raise exception 'Только creator может управлять балансом'; end if;
  if v_target='creator' then raise exception 'У creator безлимитный баланс'; end if;
  if p_amount is null or p_amount=0 or abs(p_amount)>1000000000 then raise exception 'Некорректная сумма'; end if;
  perform set_config('telechat.moon_write','allowed',true);
  update public.users set moons=moons+p_amount where lower(nick)=v_target and moons+p_amount>=0 returning moons into v_balance;
  if not found then raise exception 'Пользователь не найден или баланс станет отрицательным'; end if;
  insert into public.moon_transactions(from_nick,to_nick,amount,kind,note,created_at)
  values(v_actor,v_target,abs(p_amount),case when p_amount>0 then 'creator_grant' else 'creator_take' end,left(coalesce(p_note,''),120),v_now);
  return jsonb_build_object('ok',true,'balance',v_balance,'target',v_target);
end;
$$;

create or replace function public.telechat_moon_history(p_nick text,p_limit integer default 50)
returns table(id bigint,from_nick text,to_nick text,amount bigint,kind text,gift_id text,note text,created_at bigint)
language sql security definer set search_path=public as $$
  select t.id,t.from_nick,t.to_nick,t.amount,t.kind,t.gift_id,t.note,t.created_at
  from public.moon_transactions t
  where lower(t.from_nick)=lower(p_nick) or lower(t.to_nick)=lower(p_nick)
  order by t.created_at desc
  limit least(greatest(coalesce(p_limit,50),1),100);
$$;

