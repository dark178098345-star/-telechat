create table if not exists public.moon_gifts (
  id text primary key,
  name text not null,
  icon text not null,
  price bigint not null check (price > 0),
  rarity text not null default 'common',
  theme text not null default 'violet',
  limited boolean not null default false,
  available_until bigint,
  sort_order integer not null default 0
);

create table if not exists public.user_gifts (
  id bigint generated by default as identity primary key,
  owner_nick text not null,
  sender_nick text not null,
  gift_id text not null references public.moon_gifts(id),
  gift_name text not null,
  gift_icon text not null,
  price bigint not null,
  rarity text not null,
  theme text not null,
  limited boolean not null default false,
  message text not null default '',
  created_at bigint not null
);

create table if not exists public.moon_transactions (
  id bigint generated by default as identity primary key,
  from_nick text not null,
  to_nick text not null,
  amount bigint not null check (amount > 0),
  kind text not null check (kind in ('transfer','gift','creator_grant','creator_take')),
  gift_id text,
  note text not null default '',
  created_at bigint not null
);

create index if not exists user_gifts_owner_created_idx on public.user_gifts(owner_nick, created_at desc);
create index if not exists user_gifts_sender_limited_idx on public.user_gifts(sender_nick, limited, created_at desc);
create index if not exists moon_transactions_from_idx on public.moon_transactions(from_nick, created_at desc);
create index if not exists moon_transactions_to_idx on public.moon_transactions(to_nick, created_at desc);

